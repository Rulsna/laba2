def example(color):
    if color == "green":
        return "This is grass"
    elif color == "blue":
        return "This is water"
    elif color == "red":
        return "This is fire"
    elif color == "yellow":
        return "This is sun"
    else:
        return "I don't know"
what_is_it = example('red')
print(what_is_it)

